require 'rails'
require 'iso3166'

module ActionView
  module Helpers    
    module FormOptionsHelper
      def country_select(object, method, priority_countries = nil, options = {}, html_options = {})
        InstanceTag.new(object, method, self, options.delete(:object)).to_country_select_tag(priority_countries, options, html_options)
      end
      def country_options_for_select(selected = nil, priority_countries = nil)
        country_options = ""

        if priority_countries
          country_options += options_for_select(priority_countries, selected)
          country_options += "<option value=\"\" disabled=\"disabled\">-------------</option>\n"
          selected=nil if priority_countries.include?(selected)
        end
        
        countries = Iso3166.codes.map {|code|
          local_country = Iso3166.localize(code)
          [local_country, code] unless priority_countries.include?(local_country)
        }.compact
               
        return country_options.html_safe + options_for_select(countries, selected).html_safe
      end
    end
    
    class InstanceTag
      def to_country_select_tag(priority_countries, options, html_options)
        html_options = html_options.stringify_keys
        add_default_name_and_id(html_options)
        value = value(object)
        content_tag("select",
          add_options(
            country_options_for_select(value, priority_countries),
            options, value
          ), html_options
        )
      end
    end
    
    class FormBuilder
      def country_select(method, priority_countries = nil, options = {}, html_options = {})
        @template.country_select(@object_name, method, priority_countries, options.merge(:object => @object), html_options)
      end
    end
  end
end

module IsoCountrySelect
  extend ActiveSupport::Concern
  
  def country_name(code)
    Iso3166.localize(code)
  end
  
end

ActionView::Base.send :include, IsoCountrySelect
